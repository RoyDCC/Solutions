#Array plus array - 8 kyu
def array_plus_array(arr1,arr2):
    return sum(arr1)+sum(arr2)


#Vexing Vanishing Values - 8 kyu
def mul_by_n(lst: list, n: int) -> list:
    return [i * n for i in lst]


